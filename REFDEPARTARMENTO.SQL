CREATE TABLE DEPARTAMENTOS(
        DIRETOR CHAR(15),
        
        NOME_DE_DEPARTAMENTO VARCHAR(15) NOT NULL,
        
        NUMERO_FUNCIONARIOS INT,
        
        ORCAMENTO FLOAT NOT NULL,
        
        PRIMARY KEY (NOME_DE_DEPARTAMENTO),
        
        FOREIGN KEY (NOME) REFERENCES FUNCIONARIO (NOME),
        
        FOREIGN KEY (NOME_DO_MEIO) REFERENCES FUNCIONARIO (NOME_DO_MEIO),
        
        FOREIGN KEY (ULTIMO NOME) REFERENCES FUNCIONARIO (ULTIMO_NOME)
        );
